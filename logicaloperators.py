a=float(input("enter  first number:"))
b=float(input("enter second number:"))
 
# Comparison Operators
print("a == b:", a == b)  # Output: a == b: False
print("a != b:", a != b)  # Output: a != b: True
print("a < b:", a < b)    # Output: a < b: False
print("a > b:", a > b)    # Output: a > b: True
print("a <= b:", a <= b)  # Output: a <= b: False
print("a >= b:", a >= b)  # Output: a >= b: True